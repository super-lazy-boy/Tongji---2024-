/***************************************************************************
  函数名称：
  功    能：递归法求两数的最大公约数
  输入参数：
  返 回 值：
  说    明：1、函数名、形参、返回类型均不准动
			2、不允许出现任何形式的循环（while、do-while、for、if-goto）
***************************************************************************/
int GCD(int m, int n)
{
	int middle = 0;//用于过渡的中间值
	if (m >= n) {
		middle = n;
		n = m % n;
		m = middle;
		if (n == 0)
			return m;
		else
			return GCD(m, n);
	}
	else {
		middle = m;
		m = n % m;
		n = middle;
		if (m == 0)
			return n;
		else
			return GCD(m, n);
	}
}

/***************************************************************************
  函数名称：
  功    能：求两数的最小公倍数（不一定要递归，但要遵守要求）
  输入参数：
  返 回 值：
  说    明：1、函数名、形参、返回类型均不准动
			2、不允许出现任何形式的循环（while、do-while、for、if-goto）
***************************************************************************/
int LCM(int m, int n)
{
	int gongyue = 0;
	gongyue = GCD(m, n);
	return m * n / GCD(m, n);
}

